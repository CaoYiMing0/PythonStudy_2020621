"""
用于把字典dict2的键/值对更新到dict里
语法如下：
    dict.update(dict2)
此语法中dict代表指定字典，dict2代表添加到指定字典dict里的字典。
该方法没有任何返回值。
"""

zbc = {'卢本伟': '1001', '五五开': '1002', 'white': '1003'}
zbc2 = {'无情哈拉少': '小鸟伏特加'}
zbc.update(zbc2)
print(zbc)  # {'卢本伟': '1001', '五五开': '1002', 'white': '1003', '无情哈拉少': '小鸟伏特加'}
zbc3 = {'无情哈拉少': '猫灯伏特加'}
# 对相同项覆盖
zbc.update(zbc3)
print(zbc)  # {'卢本伟': '1001', '五五开': '1002', 'white': '1003', '无情哈拉少': '猫灯伏特加'}
